<?php
session_start();
require 'connect.php';

if ($_SERVER['REQUEST_METHOD'] == 'POST') {
    $name = $_POST['username'];
    $email = $_POST['email'];
    $password = password_hash($_POST['password'], PASSWORD_DEFAULT);

    // Perform basic validation
    if (empty($name) || empty($email) || empty($_POST['password'])) {
        echo "All fields are required";
        exit;
    }

    // More validation (e.g., email format validation) can be added here

    $sql = "INSERT INTO users (name, email, password) VALUES (?, ?, ?)";
    $stmt = $conn->prepare($sql);
    $stmt->bind_param("sss", $name, $email, $password);

    if ($stmt->execute()) {
        header("Location: index.php"); // Corrected the file name to lowercase
        exit;
    } else {
        // Log the error instead of displaying it to the user
        error_log("Error: " . $sql . "<br>" . $conn->error);
        echo "Something went wrong during registration. Please try again later.";
    }

    $stmt->close();
    $conn->close();
}
